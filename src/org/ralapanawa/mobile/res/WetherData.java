package org.ralapanawa.mobile.res;

public class WetherData {
	
	private String obTime;
	private String temp;
	private String wetherDesc;
	private String windSpeed;
	private String imageLoc;
	
	
	
	public String getImageLoc() {
		return imageLoc;
	}
	public void setImageLoc(String imageLoc) {
		this.imageLoc = imageLoc;
	}
	public String getObTime() {
		return obTime;
	}
	public void setObTime(String obTime) {
		this.obTime = obTime;
	}
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	public String getWetherDesc() {
		return wetherDesc;
	}
	public void setWetherDesc(String wetherDesc) {
		this.wetherDesc = wetherDesc;
	}
	public String getWindSpeed() {
		return windSpeed;
	}
	public void setWindSpeed(String windSpeed) {
		this.windSpeed = windSpeed;
	}
	
	

}
